import asyncio

from dotenv import load_dotenv

from db import Database

load_dotenv()


async def seed():
    db = Database()
    await db.connect()
    await db.create_academy_table()

    await db.add_academy_article(
        day=21,
        month=12,
        title="Дихотомія контролю: Фундамент свободи",
        content=(
            "Головне завдання у житті — просто розділити речі на дві категорії: "
            "зовнішні, які ми не можемо контролювати, та рішення, які ми приймаємо щодо них. "
            "Епіктет вчив: «Одні речі залежать від нас, а інші — ні». До того, що залежить, "
            "належать наші думки, прагнення та власні дії. Все інше — тіло, майно, "
            "репутація — поза нашою владою.\n\n"
            "Велика помилка людини — витрачати психічну енергію на те, що вона не може змінити. "
            "Коли ти хвилюєшся через затори чи думку сусіда — ти стаєш рабом. "
            "Коли ти фокусуєшся на своїй реакції — ти стаєш вільним."
        ),
        reflection="Випиши сьогодні одну річ, яка тебе дратує, але на яку ти не можеш вплинути. Скажи собі: «Це не в моїй владі» — і відпусти її.",
    )
    print("✅ Статтю для Академії додано!")
    await db.pool.close()


if __name__ == "__main__":
    asyncio.run(seed())
